/* Comparison Operator */

let a = 10;
let b = 12;

/* JavaScript akan mengevaluasi kedua nilai tersebut dan mengembalikan boolean dengan nilai hasil perbandingan tersebut, baik false atau true */

console.log(a < b);
console.log(a > b);

//Perbedaan antara “Sama” dan “Identik”
const aString = '10';
const aNumber = 10

console.log(aString == aNumber) // true, karena nilainya sama-sama 10
console.log(aString === aNumber) // false, karena walaupun nilainya sama, tetapi tipe datanya berbeda
