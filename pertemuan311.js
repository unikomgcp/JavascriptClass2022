/* Operator */

let x = 10;
let y = 5

//contoh alternatif penambahan x dan y (x = x + y)
x += y;

console.log(x);

//contoh alternatif lainnya, bisa dicoba satu persatu
x += y; // artinya -> x = x + y;
x -= y; // artinya -> x = x - y;
x *= y; // artinya -> x = x * y;
x /= y; // artinya -> x = x / y;
x %= y; // artinya -> x = x % y;